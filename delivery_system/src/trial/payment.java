    private String generatePaymentId() {
        String paymentId = null;
        try {
            // Generate a random 4-digit number
            Random random = new Random();
            int randomNum = random.nextInt(10000); // Generates 0 to 9999
            String formattedRandomNum = String.format("%04d", randomNum); // Format to 4 digits with leading zeros
            
            // Construct the payment ID
            paymentId = "P" + formattedRandomNum;
            
            // Check if payment ID already exists (ensure uniqueness)
            while (isPaymentIdExists(paymentId)) {
                randomNum = random.nextInt(10000); // Regenerate random number
                formattedRandomNum = String.format("%04d", randomNum);
                paymentId = "P" + formattedRandomNum;
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error generating payment ID.");
        }
        return paymentId;
    }

    /**
     * Checks if the given payment ID already exists in the database.
     * @param paymentId Payment ID to check
     * @return True if payment ID exists, false otherwise
     */
    private boolean isPaymentIdExists(String paymentId) {
        boolean exists = false;
        try {
            String sql = "SELECT COUNT(*) AS count FROM payment WHERE pay_id = ?";
            ps = con.prepareStatement(sql);
            ps.setString(1, paymentId);
            ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                int count = rs.getInt("count");
                exists = (count > 0);
            }
        } catch (SQLException ex) {
            Logger.getLogger(payment.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        return exists;
    }
    
    
