    private void generateBill(String orderId, String paymentId) {
    try {
            
            String restaurantName = name;

            StringBuilder bill = new StringBuilder();
            bill.append("********** ").append(restaurantName).append(" **********\n");
            bill.append("Order ID: ").append(orderId).append("\n");
            bill.append("Payment ID: ").append(paymentId).append("\n");
            bill.append("-------------------------------------\n");
            bill.append(String.format("%-10s %-20s %5s %10s\n", "Item ID", "Item", "Qty", "Price"));
            bill.append("-------------------------------------\n");

            double totalCost = 0.0;

            for (MenuItem item : selectedItemsList) {
                String itemId = item.getItemId();
                String itemName = item.getName();
                int quantity = item.getQuantity();
                double price = item.getPrice();
                totalCost += quantity * price;
                bill.append(String.format("%-10s %-20s %5d %10.2f\n", itemId, itemName, quantity, price));
            }

            bill.append("-------------------------------------\n");
            bill.append(String.format("Total: %38.2f\n", totalCost));
            bill.append("\n*** Thank You ***");

            JTextArea billArea = new JTextArea(bill.toString());
            billArea.setEditable(false);
            JScrollPane scrollPane = new JScrollPane(billArea);

            JFrame billFrame = new JFrame("Bill");
            billFrame.add(scrollPane);
            billFrame.setSize(400, 600);
            billFrame.setVisible(true);
            new delivery_person(name,username,orderId).setVisible(true);
        } catch (Exception ex) {
            Logger.getLogger(payment.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
}     
